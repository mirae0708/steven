function predictCell(history, currentGrid, targetCellIdx, inputBuf) {
    const n = currentGrid.length;
    const findInHistory = (depth) => {
        const counter = {};
        const key = JSON.stringify(currentGrid.slice(n-depth, n));
        history.forEach(game => {
            for(let i=depth; i<game.length; i++) {
                if(JSON.stringify(game.slice(i-depth, i)) === key) {
                    const row = game[i];
                    let match = true;
                    for(let c=0; c<targetCellIdx; c++) if(row[c] !== inputBuf[c]) { match = false; break; }
                    if(match) {
                        counter[row[targetCellIdx]] = (counter[row[targetCellIdx]] || 0) + 1;
                    }
                }
            }
        });
        const res = Object.entries(counter).sort((a,b) => b[1]-a[1]);
        return res.length > 0 ? res[0][0] : null;
    };
    return findInHistory(4) || findInHistory(3) || findInHistory(2) || "B";
}

let table = [];
let gCurr = [];
for (let i = 0; i < IMAGE3.length; i++) {
    const actual = IMAGE3[i];
    if (i < 4) {
        table.push({ col: i+1, actual: actual.join(""), pred2: "-", pred3: "-", result: "준비" });
        gCurr.push(actual);
        continue;
    }

    const p2 = predictCell(GAMES, gCurr, 1, [actual[0]]);
    let win = false;
    let p3 = "-";
    
    if (p2 === actual[1]) {
        win = true;
        p3 = "-";
    } else {
        p3 = predictCell(GAMES, gCurr, 2, [actual[0], actual[1]]);
        if (p3 === actual[2]) win = true;
    }

    table.push({
        col: i+1,
        actual: actual.join(""),
        pred2: p2,
        pred3: p3,
        result: win ? "적중" : "DEATH"
    });
    gCurr.push(actual);
}
